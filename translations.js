function detectLanguage() {
  const saved = localStorage.getItem("rs_language");
  if (saved) return saved;
  const browser = (navigator.language || navigator.userLanguage).split("-")[0];
  const supported = ["en","hi","hinglish"];
  return supported.includes(browser) ? browser : "en";
}

function applyLanguage(lang) {
  const t = translations[lang] || translations["en"];
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (t[key]) el.textContent = t[key];
  });
  localStorage.setItem("rs_language", lang);
  document.documentElement.setAttribute("lang", lang);
  document.documentElement.setAttribute("dir", "ltr");
  
  // Update language selector button text
  const langBtn = document.getElementById("lang-selector-btn");
  if (langBtn) {
    langBtn.textContent = lang === "hinglish" ? "HI-EN" : lang.toUpperCase();
  }
  
  // Close dropdown
  const dropdown = document.getElementById("lang-dropdown");
  if (dropdown) dropdown.classList.remove("show");
}

